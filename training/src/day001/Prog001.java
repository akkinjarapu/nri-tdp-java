package day001;

public class Prog001 {

	public static void main(String[] args) {
		/* var keyword introduced in Java 10 */
		var a = 100;
		
		/* type <identifier> [ = value] */
		int b = 200;
		
		/* two variables printed in two separate lines */
		System.out.println(a);
		
		System.out.println(b);

	}

}
