package day003;

public class Prog003 {
	
	public static void main(String[] args) {
		// Byte (wrapper) - byte (primitive)
		// Short          - short
		// Integer        - int
		// Long           - long
		
		System.out.println(Byte.BYTES + " bytes or " + Byte.SIZE + " bits");
		System.out.println(Short.BYTES + " bytes or " + Short.SIZE + " bits");
		System.out.println(Integer.BYTES + " bytes or " + Integer.SIZE + " bits");
		System.out.println(Long.BYTES + " bytes or " + Long.SIZE + " bits");
	}
	
}
