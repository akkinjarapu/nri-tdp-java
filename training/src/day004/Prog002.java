package day004;

public class Prog002 {
	
	public static void main(String[] args) {
		char ch = 'A';
		System.out.println(ch);
		
		ch = '\n';
		System.out.println(ch);
		
		ch = 65;
		System.out.println(ch);
		
		ch = 0X41;
		System.out.println(ch);
		
		ch = 0b1000001;
		System.out.println(ch);
		
		ch = '\u00A3';
		System.out.println(ch);
	}
	
}
