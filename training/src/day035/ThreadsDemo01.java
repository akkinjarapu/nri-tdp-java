package day035;

public class ThreadsDemo01 {

	public static void main(String[] args) {

		for (int i = 101; i <= 199; i++) {
			System.out.print(i + " ");
		}

		System.out.println();

		for (int i = 201; i <= 299; i++) {
			System.out.print(i + " ");
		}

		System.out.println();

		for (int i = 301; i <= 399; i++) {
			System.out.print(i + " ");
		}
	}

}
